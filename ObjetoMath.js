//Elevado a la potencia Math.pow
console.log(Math.pow(2, 3));

//Redondear
//Math.round redondea al entero mas cerca
//0
console.log(Math.round(0.49));
//1
console.log(Math.round(0.50));
//Math.floor redondea al entero hacia abajo
//5
console.log(Math.floor(5.92));
//Math.ceil redondea al entero hacia arriba
//4
console.log(Math.ceil(3.27));

//Math.max y Math.min nos envia el maximo o el minimo de un conjunto de numeros
//Math.max
//5
console.log(Math.max(1, 4, 2, 5, 3));
//Math.min
//1
console.log(Math.min(1, 4, 2, 5, 3));

//Math.random cualquier numero entre 0 y 1
console.log(Math.random());
